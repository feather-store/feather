"""Render Markdown report from accumulated bench/results/*.json."""
from __future__ import annotations
from pathlib import Path
from .runner import load_results, REPORTS_DIR


HEADER = """# Feather DB Benchmark Report

Generated by `python -m bench report`. One row per scenario run; most recent first.
"""

COLS = [
    ("scenario", "Scenario"),
    ("dataset", "Dataset"),
    ("n", "N"),
    ("dim", "Dim"),
    ("feather_version", "Ver"),
    ("commit", "Commit"),
]


def _fmt(v):
    if isinstance(v, float):
        return f"{v:.3f}"
    return str(v)


def render() -> str:
    results = load_results()
    results.sort(key=lambda r: r.get("started_at", 0), reverse=True)

    lines = [HEADER, ""]

    if not results:
        lines.append("_No results yet._")
        return "\n".join(lines)

    # Group by scenario
    by_scenario: dict[str, list[dict]] = {}
    for r in results:
        by_scenario.setdefault(r["scenario"], []).append(r)

    for scenario, runs in by_scenario.items():
        lines.append(f"## {scenario}")
        lines.append("")
        # Collect metric keys union
        metric_keys: list[str] = []
        seen: set[str] = set()
        for r in runs:
            for k in r.get("metrics", {}):
                if k not in seen:
                    seen.add(k)
                    metric_keys.append(k)

        header = [c[1] for c in COLS] + metric_keys
        lines.append("| " + " | ".join(header) + " |")
        lines.append("| " + " | ".join("---" for _ in header) + " |")

        for r in runs:
            row = [_fmt(r.get(k[0], "")) for k in COLS]
            for mk in metric_keys:
                row.append(_fmt(r.get("metrics", {}).get(mk, "")))
            lines.append("| " + " | ".join(row) + " |")
        lines.append("")

    return "\n".join(lines)


def write_report(path: Path | None = None) -> Path:
    REPORTS_DIR.mkdir(exist_ok=True)
    path = path or (REPORTS_DIR / "latest.md")
    path.write_text(render())
    return path
